package com.rotef.game.world.light;

public class LightingSwipe {

	public final int x;
	public final int y;
	public final int width;
	public final int height;

	public LightingSwipe(int x, int y, int width, int height) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}

}
