package com.rotef.game.world;

public class WorldLoadingException extends Exception {

	private static final long serialVersionUID = 1L;

	public WorldLoadingException() {
		super();
	}

	public WorldLoadingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public WorldLoadingException(String arg0) {
		super(arg0);
	}

	public WorldLoadingException(Throwable arg0) {
		super(arg0);
	}

}
