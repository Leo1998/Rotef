package com.rotef.game.util.file;

import java.io.IOException;

public class DFAException extends IOException {
	private static final long serialVersionUID = 1L;

	public DFAException() {
		super();
	}

	public DFAException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DFAException(String arg0) {
		super(arg0);
	}

	public DFAException(Throwable arg0) {
		super(arg0);
	}

}
